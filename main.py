
# control structures
# if, elif, else

# if <condition>:
#     <code>
# elif <condition>:
#     <code>
# else:
#     <code>

number = int(input("Enter any number from 0 to 10 : "))
if number < 10:
    print("Number is less than 10")
elif number > 10:
    print("Number is greater than 10")
else:
    print("Number is equal to 10")

